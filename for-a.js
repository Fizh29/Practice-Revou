// Program untuk menampilkan bilangan ganjil dari 1 hingga 10
console.log("Bilangan ganjil dari 1 hingga 10:");
// deklarasi ; pengecehakn kondisi ; after statement; 

for (let i = 1; i <= 10; i++) { //kalo kondisi nya masih memenuhi (true) maka bloknya akan terus berjalan kalo udah false maka berhenti
    if (i % 2 !== 0) {
        console.log(i);
    }
}
